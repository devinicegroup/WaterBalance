import UIKit

extension UIColor {
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }
    
    static func mainDark() -> UIColor {
        return #colorLiteral(red: 0.137254902, green: 0.1843137255, blue: 0.2784313725, alpha: 1)
    }
    
    static func mainSecondary() -> UIColor {
        return #colorLiteral(red: 0.2431372549, green: 0.3607843137, blue: 0.4745098039, alpha: 1)
    }
    
    static func lightMenu() -> UIColor {
        return #colorLiteral(red: 0.8352941176, green: 0.8588235294, blue: 0.9058823529, alpha: 1)
    }
    
    static func blue() -> UIColor {
        return #colorLiteral(red: 0.5647058824, green: 0.8117647059, blue: 0.9450980392, alpha: 1)
    }
    
    static func pink() -> UIColor {
        return #colorLiteral(red: 0.9647058824, green: 0.5137254902, blue: 0.5529411765, alpha: 1)
    }
    
    static func green() -> UIColor {
        return #colorLiteral(red: 0.8470588235, green: 0.9254901961, blue: 0.5176470588, alpha: 1)
    }
    
    static func orange() -> UIColor {
        return #colorLiteral(red: 0.9921568627, green: 0.631372549, blue: 0.2705882353, alpha: 1)
    }
    
    static func gray() -> UIColor {
        return #colorLiteral(red: 0.8039215686, green: 0.8588235294, blue: 0.9058823529, alpha: 1)
    }
    
    static func typographyPrimary() -> UIColor {
        return #colorLiteral(red: 0.137254902, green: 0.1843137255, blue: 0.2784313725, alpha: 1)
    }
    
    static func typographySecondary() -> UIColor {
        return #colorLiteral(red: 0.431372549, green: 0.4823529412, blue: 0.5803921569, alpha: 1)
    }
    
    static func typographyLight() -> UIColor {
        return #colorLiteral(red: 0.8901960784, green: 0.9019607843, blue: 0.9294117647, alpha: 1)
    }
}
